/* ================================================================== */
/*  THEME SETUP                                                       */
/* ================================================================== */
function lns_theme_setup(): void {
    add_theme_support( 'title-tag' );
    add_theme_support( 'post-thumbnails' );
    add_theme_support( 'html5', [ 'search-form', 'comment-form', 'gallery', 'caption', 'style', 'script' ] );
    add_theme_support( 'custom-logo' );

    register_nav_menus( [
        'primary' => __( 'Primary Navigation', 'lawrence-sons' ),
    ] );
}
add_action( 'after_setup_theme', 'lns_theme_setup' );

/* ================================================================== */
/*  ENQUEUE STYLES & SCRIPTS                                          */
/* ================================================================== */
function lns_enqueue_assets(): void {
    /* Google Fonts – Poppins */
    wp_enqueue_style(
        'lns-google-fonts',
        'https://fonts.googleapis.com/css2?family=Poppins:wght@300;400;500;600;700;800&display=swap',
        [],
        null
    );

    /* Main stylesheet */
    wp_enqueue_style(
        'lns-style',
        get_stylesheet_uri(),
        [ 'lns-google-fonts' ],
        wp_get_theme()->get( 'Version' )
    );

    /* Main JS */
    wp_enqueue_script(
        'lns-main',
        get_template_directory_uri() . '/assets/js/main.js',
        [],
        wp_get_theme()->get( 'Version' ),
        true
    );
}
add_action( 'wp_enqueue_scripts', 'lns_enqueue_assets' );

/* ================================================================== */
/*  CUSTOMIZER – editable contact info & hero                         */
/* ================================================================== */
function lns_customize_register( WP_Customize_Manager $wp_customize ): void {

    /* --- Section: Contact Info --- */
    $wp_customize->add_section( 'lns_contact', [
        'title'    => 'Contact Information',
        'priority' => 30,
    ] );

    $fields = [
        'lns_phone'         => [ 'Phone Number',   '(905) 555-0123' ],
        'lns_email'         => [ 'Email Address',   'info@lawrenceandsons.com' ],
        'lns_service_area'  => [ 'Service Area',    'Serving the Greater Houston, TX Area' ],
    ];

    foreach ( $fields as $id => [ $label, $default ] ) {
        $wp_customize->add_setting( $id, [
            'default'           => $default,
            'sanitize_callback' => 'sanitize_text_field',
        ] );
        $wp_customize->add_control( $id, [
            'label'   => $label,
            'section' => 'lns_contact',
            'type'    => 'text',
        ] );
    }

    /* --- Section: Hero --- */
    $wp_customize->add_section( 'lns_hero', [
        'title'    => 'Hero Section',
        'priority' => 31,
    ] );

    $wp_customize->add_setting( 'lns_hero_image', [
        'default'           => '',
        'sanitize_callback' => 'esc_url_raw',
    ] );
    $wp_customize->add_control( new WP_Customize_Image_Control( $wp_customize, 'lns_hero_image', [
        'label'   => 'Hero Background Image',
        'section' => 'lns_hero',
    ] ) );
}
add_action( 'customize_register', 'lns_customize_register' );

/* ================================================================== */
/*  HELPER: get theme mod with fallback                               */
/* ================================================================== */
function lns_get( string $key, string $fallback = '' ): string {
    return get_theme_mod( $key, $fallback );
}

